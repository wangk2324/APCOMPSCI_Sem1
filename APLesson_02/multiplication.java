public class multiplication
{
	public static void main(String[]args)
	{
		int x = 2;
		int y = 4;
		int xy = x * y;
		
		System.out.println(x + " multiplied by " + y + " is " + xy);
		
		x = 5;
		y = 6;
		xy = x * y;
		
		System.out.println(x + " and " + y + " would produce...");
		System.out.println(x + " multiplied by " + y + " is " + xy);
	}
}